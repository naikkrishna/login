package com.example.login;

public class item {
    String name;
    int price;
    int qty;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getQty() {
        return qty;
    }



    public void setQty(int qty) {
        this.qty = qty;
    }
}



